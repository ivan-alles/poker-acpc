using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ai.lib.algorithms
{
    /// <summary>
    /// Various operations with a short sequences of objects.
    /// </summary>
    public static class ShortSequence
    {
        public static void Swap<T>(ref T lhs, ref T rhs)
        {
            T temp;
            temp = lhs;
            lhs = rhs;
            rhs = temp;
        }
    }
}